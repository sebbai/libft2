#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		n /= 10;
		ft_putnbr(n);
		ft_putchar('8');
	}
	else if ((n >= 0) && (n <= 9))
		ft_putchar(n + '0');
	else if (n < 0)
	{
		ft_putchar('-');
		ft_putnbr(n * (-1));
	}
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
